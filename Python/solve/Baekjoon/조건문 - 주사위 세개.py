# 주사위 세개 - 조건문 활용 문제

# 첫째 줄에 3개의 눈이 빈칸을 사이에 두고 각각 주어질 때,
# 3개가 모두 같으면 10,000원+(같은 눈)×1,000을,
# 3개 중 2개만 같으면 1,000원+(같은 눈)×100을
# 3개가 모두 다르면 (그 중 가장 큰 눈)×100을 출력하는 문제

# 전역 변수 선언
n1, n2, n3 = map(int, input('').split())
# 조건문
if n1 == n2 and n2 == n3 : # 3개가 모두 같은 경우
    print(10000 + n1 * 1000)
elif n1 == n2 and n1 != n3 and n2 != n3 : # 2개가 같은 경우 중 n1, n2만 같은 경우
    print(1000 + n1 * 100)
elif n2 == n3 and n1 != n2 and n1 != n3 : # 2개가 같은 경우 중 n2, n3만 같은 경우
    print(1000 + n2 * 100)
elif n1 == n3 and n1 != n2 and n2 != n3 : # 2개가 같은 경우 중 n1, n3만 같은 경우
    print(1000 + n1 * 100)
else : # 3개가 모두 다른 경우
    list_ = [n1, n2, n3]
    print(max(list_)*100)

