# 추가적으로 max() 함수
# 리스트나 튜플에만 사용할 수 있는 것이 아니라 그냥 숫자들에도 쓸 수 있음
# max(a, b, c) 이런 식으로!

# 간결하게 고친 코드
if n1 == n2 and n2 == n3 : # 3개가 모두 같은 경우
    print(10000 + n1 * 1000)
elif n1 == n2 : # 2개가 같은 경우 중 n1, n2만 같은 경우
    print(1000 + n1 * 100)
elif n2 == n3 : # 2개가 같은 경우 중 n2, n3만 같은 경우
    print(1000 + n2 * 100)
elif n1 == n3 : # 2개가 같은 경우 중 n1, n3만 같은 경우
    print(1000 + n1 * 100)
else : # 3개가 모두 다른 경우
    print(max(n1, n2, n3)*100)

